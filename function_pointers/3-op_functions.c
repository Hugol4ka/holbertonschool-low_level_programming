#include"3-calc.h"
#include<stdio.h>
#include<stdlib.h>

/**
* op_add - Calcule la somme de deux entiers
* @a: le premier entier
* @b: le second entier
*
* Return: le resultat de a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - Calcule la soustraction de deux entiers
* @a: le premier entier
* @b: le second entier
*
* Return: le resultat de a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - Calcule la multiplication de deux entiers
* @a: le premier entier
* @b: le second entier
*
* Return: le resultat de a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - Calcule la division de deux entiers
* @a: le premier entier
* @b: le second entier
*
* Return: le resultat de a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - Calcule le modulo de deux entiers
* @a: le premier entier
* @b: le second entier
*
* Return: le resultat de a % b
*/
int op_mod(int a, int b)
{
return (a % b);
}
